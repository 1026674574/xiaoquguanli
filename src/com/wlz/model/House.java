package com.wlz.model;

public class House {
    private int ho_id;
    private int ho_dong;
    private  int ho_dan;
    private  int ho_hao;
    private  int ho_area;
    private int ho_state;
    private String ho_type;

    public String getHo_type() {
        return ho_type;
    }

    public void setHo_type(String ho_type) {
        this.ho_type = ho_type;
    }

    public int getHo_state() {
        return ho_state;
    }

    public void setHo_state(int ho_state) {
        this.ho_state = ho_state;
    }

    public int getHo_id() {
        return ho_id;
    }

    public void setHo_id(int ho_id) {
        this.ho_id = ho_id;
    }

    public int getHo_dong() {
        return ho_dong;
    }

    public void setHo_dong(int ho_dong) {
        this.ho_dong = ho_dong;
    }

    public int getHo_dan() {
        return ho_dan;
    }

    public void setHo_dan(int ho_dan) {
        this.ho_dan = ho_dan;
    }

    public int getHo_hao() {
        return ho_hao;
    }

    public void setHo_hao(int ho_hao) {
        this.ho_hao = ho_hao;
    }

    public int getHo_area() {
        return ho_area;
    }

    public void setHo_area(int ho_area) {
        this.ho_area = ho_area;
    }
}
